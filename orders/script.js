'use strict';

function show_order_trac(){

    let order_trac = document.getElementsByClassName('order-trac-detail')[0];
    order_trac.style.display = 'flex';
}


function close_order_trac(){

    let order_trac = document.getElementsByClassName('order-trac-detail')[0];
    order_trac.style.display = 'none';
}